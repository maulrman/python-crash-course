loc_list = [
        "Dallas",
        "Melbourne",
        "Singapore",
        "Tokyo",
        "Sydney",
        "Penang",
        "New York"
]

print(loc_list)
print("sorted()")
print(sorted(loc_list))
print("sorted() reverse")
print(sorted(loc_list, reverse=True))
print("reverse()")
print(loc_list)
loc_list.reverse()
print(loc_list)
print("sort()")
loc_list.sort()
print(loc_list)
